package tr.com.idefix.android.internal.di.components;

import dagger.Component;
import tr.com.idefix.android.internal.di.PerActivity;
import tr.com.idefix.android.internal.di.modules.ActivityModule;
import tr.com.idefix.android.internal.di.modules.FilterActivityModule;
import tr.com.idefix.android.view.activities.FilterActivity;

/**
 * A scope {@link PerActivity} component.
 * Injects user specific Fragments.
 */
@PerActivity @Component(
    dependencies = ApplicationComponent.class,
    modules = {
        ActivityModule.class, FilterActivityModule.class
    }) public interface FilterActivityComponent extends ActivityComponent {

  void inject(FilterActivity filterActivity);
}
